def seconds_to_timedelta(seconds: Union[int, float]) -> timedelta:
    days, seconds = divmod(seconds, SECONDS_PER_DAY)
    fractional_seconds, integer_seconds = modf(seconds)
    microseconds = fractional_seconds * 1e6
    return timedelta(days=days, seconds=integer_seconds, microseconds=microseconds)
