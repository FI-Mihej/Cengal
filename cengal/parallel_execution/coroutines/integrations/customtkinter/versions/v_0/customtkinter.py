def prepare_mainloop(app: customtkinter.CTk):
    if '5.0.0' > customtkinter.__version__:
        if app.window_exists is False:
            app.deiconify()
            app.window_exists = True
    else:
        if not app._window_exists:
            if sys.platform.startswith("win"):
                app._windows_set_titlebar_color(app._get_appearance_mode())

                if not app._withdraw_called_before_window_exists and not app._iconify_called_before_window_exists:
                    app.deiconify()

            app._window_exists = True
